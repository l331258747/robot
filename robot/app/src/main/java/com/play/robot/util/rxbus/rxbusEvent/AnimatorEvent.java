package com.play.robot.util.rxbus.rxbusEvent;

public class AnimatorEvent {
    boolean small;
    boolean big;

    public boolean isSmall() {
        return small;
    }

    public void setSmall(boolean small) {
        this.small = small;
    }

    public boolean isBig() {
        return big;
    }

    public void setBig(boolean big) {
        this.big = big;
    }
}
